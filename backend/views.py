@api_view(['POST'])
def getProducts(request):
    query = request.data['keyword'][9:].split("&")[0]
    category = request.data['category']

    if query is None:
        query = ''

    if category is None:
        category = ''

    if category == '':
        products = Product.objects.filter(
            name__icontains=query).order_by('createdAt')
    else:
        products = Product.objects.filter(
            name__icontains=query, category=category).order_by('createdAt')

    summer = products.filter(category='Summer').count()
    winter = products.filter(category='Winter').count()

    page = request.query_params.get('page')
    paginator = Paginator(products, 10)

    # try:
    #     products = paginator.page(page)
    # except PageNotAnInteger:
    #     products = paginator.page(10)
    # except EmptyPage:
    #     products = paginator.page(paginator.num_pages)

    if page is None:
        page = 1

    page = int(page)
    serializer = ProductSerializer(products, many=True)

    return Response(
        {'products': serializer.data, 'page': page,
         'pages': paginator.num_pages, 'summerCount': summer, 'winterCount': winter})


